import mongoose, { Types } from "mongoose";

const blogSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  image: {
    type: String,
    required: true
  }
}, {timestamps: true});

const Blog = mongoose.model('blogs', blogSchema);

export default Blog;